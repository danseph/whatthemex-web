var cssPack = {
    ko: './css/common.css',
    en: './css/common_en.css',
    zh: './css/common_zh.css',
    jp: ''
}
  
function getLanguage() {
    var lang = navigator.language || navigator.userLanguage;
    if(!lang) {
        return 'en';
    }
    else {
        return lang.substring(0, 2);
    }
}

function applyLanguage(lang) {
    if(!lang) {
        lang = localStorage.getItem("LANG");
        if(!lang)
            lang = getLanguage();
    }
    else {  // 유저가 설정한 언어를 저장
        localStorage.setItem("LANG", lang);
    }

    // CSS 변경
    var cssLink = cssPack[lang];
    if(!cssLink) {
        cssLink = cssPack.en;
    }
    $('link').eq(5).attr('href', cssLink);

    // 언어팩 변경
    var strPack = strings[lang];
    if(!strPack) {
        lang = 'en';
        strPack = strings.en;
    }
    
    $('.text-trans').each(function(idx){
        var strid = $(this).data('strid');
        if(strPack[strid]) {
            $(this).html(strPack[strid]);
        }
        
    });

    $('.lan-list a').each(function(){
        if(lang == $(this).data('lang')) {
            $(".lan-now").text( $(this).text().trim() );
            $(this).addClass('on');
        }            
        else
            $(this).removeClass('on');
            $(".ham-wrap").removeClass("active");
            $(".gnb.mb").removeClass("open");
            $('html, body').css({'overflow': 'auto'});
    });
}